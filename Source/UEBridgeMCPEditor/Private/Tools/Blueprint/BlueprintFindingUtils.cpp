	bool HasPinDefaultValue(const UEdGraphPin* Pin)
	{
		return Pin
			&& (Pin->DefaultObject != nullptr
				|| !Pin->DefaultValue.IsEmpty()
				|| !Pin->AutogeneratedDefaultValue.IsEmpty()
				|| !Pin->DefaultTextValue.IsEmpty()
				|| Pin->bDefaultValueIsIgnored);
	}

	bool ShouldInspectGraph(const UBlueprint* Blueprint, const UEdGraph* Graph, const BlueprintFindingUtils::FQuery& Query)
	{
		if (!Graph)
		{
			return false;
		}

		if (!Query.GraphNameFilter.IsEmpty()
			&& !Graph->GetName().Equals(Query.GraphNameFilter, ESearchCase::IgnoreCase))
		{
			return false;
		}

		if (!Query.GraphTypeFilter.IsEmpty())
		{
			const FString GraphType = McpV2ToolUtils::GetBlueprintGraphType(Blueprint, Graph);
			if (!GraphType.Equals(Query.GraphTypeFilter, ESearchCase::IgnoreCase))
			{
				return false;
			}
		}

		return true;
	}

	bool InterfaceNeedsImplementationGraph(const UClass* InterfaceClass)
	{
		if (!InterfaceClass)
		{
			return false;
		}

		const UClass* IterationClass = InterfaceClass;
		if (const UBlueprint* InterfaceBlueprint = Cast<UBlueprint>(InterfaceClass->ClassGeneratedBy))
		{
			for (UEdGraph* FunctionGraph : InterfaceBlueprint->FunctionGraphs)
			{
				if (!FunctionGraph)
				{
					continue;
				}

				for (UEdGraphNode* Node : FunctionGraph->Nodes)
				{
					if (Cast<UK2Node_FunctionResult>(Node))
					{
						return true;
					}
				}
			}

			if (InterfaceBlueprint->SkeletonGeneratedClass)
			{
				IterationClass = InterfaceBlueprint->SkeletonGeneratedClass;
			}
		}

		for (TFieldIterator<UFunction> FunctionIter(IterationClass, EFieldIteratorFlags::IncludeSuper); FunctionIter; ++FunctionIter)
		{
			const UFunction* Function = *FunctionIter;
			if (Function
				&& UEdGraphSchema_K2::CanKismetOverrideFunction(Function)
				&& !UEdGraphSchema_K2::FunctionCanBePlacedAsEvent(Function))
			{
				return true;
			}
		}

		return false;
	}

	TSharedPtr<FJsonObject> MakeFinding(
		const FString& Code,
		const FString& Severity,
		const FString& Message,
		const FString& GraphName,
		UEdGraphNode* Node,
		const FString& AssetPath,
		const FString& SessionId)
	{
		TSharedPtr<FJsonObject> Finding = MakeShareable(new FJsonObject);
		Finding->SetStringField(TEXT("code"), Code);
		Finding->SetStringField(TEXT("severity"), Severity);
		Finding->SetStringField(TEXT("message"), Message);
		Finding->SetStringField(TEXT("graph_name"), GraphName);

		if (Node)
		{
			const FString NodeGuid = Node->NodeGuid.ToString(EGuidFormats::DigitsWithHyphens);
			Finding->SetStringField(TEXT("node_guid"), NodeGuid);
			if (!SessionId.IsEmpty())
			{
				Finding->SetObjectField(
					TEXT("handle"),
					McpV2ToolUtils::MakeEntityHandle(
						TEXT("blueprint_node"),
						SessionId,
						AssetPath,
						NodeGuid,
						Node->GetNodeTitle(ENodeTitleType::ListView).ToString()));
			}
		}

		return Finding;
	}
