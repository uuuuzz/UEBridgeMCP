	TArray<TSharedPtr<FJsonValue>> CollectFindings(UBlueprint* Blueprint, const FQuery& Query)
	{
		TArray<TSharedPtr<FJsonValue>> Findings;
		if (!Blueprint)
		{
			return Findings;
		}

		const int32 MaxFindings = FMath::Max(1, Query.MaxFindings);

		for (const FBPInterfaceDescription& InterfaceDescription : Blueprint->ImplementedInterfaces)
		{
			if (Findings.Num() >= MaxFindings)
			{
				break;
			}

			const UClass* InterfaceClass = InterfaceDescription.Interface.Get();
			if (!InterfaceClass
				|| InterfaceDescription.Graphs.Num() > 0
				|| !InterfaceNeedsImplementationGraph(InterfaceClass))
			{
				continue;
			}

			const FString InterfaceName = InterfaceClass->GetName();
			Findings.Add(MakeShareable(new FJsonValueObject(
				MakeFinding(
					TEXT("missing_interface_graph"),
					TEXT("error"),
					FString::Printf(TEXT("Interface '%s' has no generated implementation graphs"), *InterfaceName),
					InterfaceName,
					nullptr,
					Query.AssetPath,
					Query.SessionId))));
		}

		TArray<UEdGraph*> Graphs;
		FMcpAssetModifier::GetAllSearchableGraphs(Blueprint, Graphs);

		for (UEdGraph* Graph : Graphs)
		{
			if (Findings.Num() >= MaxFindings || !ShouldInspectGraph(Blueprint, Graph, Query))
			{
				continue;
			}

			for (UEdGraphNode* Node : Graph->Nodes)
			{
				if (!Node || Findings.Num() >= MaxFindings)
				{
					continue;
				}

				bool bNodeHasOrphanPin = false;
				for (UEdGraphPin* Pin : Node->Pins)
				{
					if (Pin && Pin->bOrphanedPin)
					{
						Findings.Add(MakeShareable(new FJsonValueObject(
							MakeFinding(
								TEXT("orphan_pin"),
								TEXT("warning"),
								FString::Printf(TEXT("Node '%s' contains orphan pin '%s'"), *Node->GetNodeTitle(ENodeTitleType::ListView).ToString(), *Pin->PinName.ToString()),
								Graph->GetName(),
								Node,
								Query.AssetPath,
								Query.SessionId))));
						bNodeHasOrphanPin = true;
						break;
					}
				}

				if (Findings.Num() >= MaxFindings)
				{
					break;
				}

				if (!bNodeHasOrphanPin && Node->HasDeprecatedReference())
				{
					Findings.Add(MakeShareable(new FJsonValueObject(
						MakeFinding(
							TEXT("broken_or_deprecated_reference"),
							TEXT("warning"),
							FString::Printf(TEXT("Node '%s' references a deprecated or invalid member"), *Node->GetNodeTitle(ENodeTitleType::ListView).ToString()),
							Graph->GetName(),
							Node,
							Query.AssetPath,
							Query.SessionId))));
				}

				if (Findings.Num() >= MaxFindings)
				{
					break;
				}

				if (const UK2Node_CallFunction* CallFunctionNode = Cast<UK2Node_CallFunction>(Node))
				{
					if (!CallFunctionNode->GetTargetFunction())
					{
						Findings.Add(MakeShareable(new FJsonValueObject(
							MakeFinding(
								TEXT("unresolved_member_reference"),
								TEXT("error"),
								FString::Printf(TEXT("Call function node '%s' could not resolve its target function"), *Node->GetNodeTitle(ENodeTitleType::ListView).ToString()),
								Graph->GetName(),
								Node,
								Query.AssetPath,
								Query.SessionId))));
					}
				}
				else if (const UK2Node_Variable* VariableNode = Cast<UK2Node_Variable>(Node))
				{
					if (!VariableNode->GetPropertyForVariable())
					{
						Findings.Add(MakeShareable(new FJsonValueObject(
							MakeFinding(
								TEXT("unresolved_member_reference"),
								TEXT("error"),
								FString::Printf(TEXT("Variable node '%s' could not resolve its referenced property"), *Node->GetNodeTitle(ENodeTitleType::ListView).ToString()),
								Graph->GetName(),
								Node,
								Query.AssetPath,
								Query.SessionId))));
					}
				}

				if (Findings.Num() >= MaxFindings)
				{
					break;
				}

				for (UEdGraphPin* Pin : Node->Pins)
				{
					if (!Pin
						|| Pin->Direction != EGPD_Input
						|| Pin->bHidden
						|| Pin->LinkedTo.Num() > 0
						|| Pin->bNotConnectable)
					{
						continue;
					}

					if (Pin->PinType.PinCategory == UEdGraphSchema_K2::PC_Exec)
					{
						const UK2Node* K2Node = Cast<UK2Node>(Node);
						if (!K2Node || K2Node->IsNodePure())
						{
							continue;
						}

						Findings.Add(MakeShareable(new FJsonValueObject(
							MakeFinding(
								TEXT("unlinked_required_pin"),
								TEXT("error"),
								FString::Printf(TEXT("Node '%s' has an unlinked required exec pin '%s'"), *Node->GetNodeTitle(ENodeTitleType::ListView).ToString(), *Pin->PinName.ToString()),
								Graph->GetName(),
								Node,
								Query.AssetPath,
								Query.SessionId))));
					}
					else if (!HasPinDefaultValue(Pin))
					{
						Findings.Add(MakeShareable(new FJsonValueObject(
							MakeFinding(
								TEXT("missing_default_value"),
								TEXT("warning"),
								FString::Printf(TEXT("Pin '%s' on node '%s' has no link and no default value"), *Pin->PinName.ToString(), *Node->GetNodeTitle(ENodeTitleType::ListView).ToString()),
								Graph->GetName(),
								Node,
								Query.AssetPath,
								Query.SessionId))));
					}

					if (Findings.Num() >= MaxFindings)
					{
						break;
					}
				}
			}
		}

		return Findings;
	}
